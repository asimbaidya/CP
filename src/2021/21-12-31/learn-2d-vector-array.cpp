#include <bits/stdc++.h>
using namespace std;

int main() {
    vector<int> v[10][100];
    for (int i = 0; i < 10; i++) {
        // cout << v[i] << "\n";
        v[i]->push_back(10);
        for (int j = 0; j < 5; j++) {
            break;
            cout << v[i]->size() << "\n";
        }
    }

    vector<vector<int>> vi[10][10];

    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            continue;
            // cout << vi[i][j] << "\n";
        }
    }

    cout << vi[0]->size() << endl;
    cout << vi[0][0].size() << endl;

    return 0;
}
